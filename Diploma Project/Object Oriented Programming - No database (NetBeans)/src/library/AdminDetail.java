    private void showlistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showlistActionPerformed
        // TODO add your handling code here:
        File file =new File("admindetail.txt");
        
        try{
            BufferedReader reader = new BufferedReader(new FileReader("admindetail.txt"));
            String firstLine = reader.readLine().trim();
            String [] columnName = firstLine.split(" , ");
            DefaultTableModel model = (DefaultTableModel)admindetail.getModel();
            
            Object[] tableLines = reader.lines().toArray();
            for(int i = 0;i < tableLines.length;i++)
            {
                String line = tableLines[i].toString().trim();
                String[] dataRow = line.split(" , ");
                model.addRow(dataRow);
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_showlistActionPerformed

