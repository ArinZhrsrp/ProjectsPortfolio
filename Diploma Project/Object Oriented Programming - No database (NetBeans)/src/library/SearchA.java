    private void labelSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_labelSearchKeyReleased
        // TODO add your handling code here:
         DefaultTableModel model = (DefaultTableModel)table.getModel();
         String search = labelSearch.getText().toLowerCase().toUpperCase();
         TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
         table.setRowSorter(tr);
         tr.setRowFilter(RowFilter.regexFilter(search));
    }//GEN-LAST:event_labelSearchKeyReleased

    private void labelSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelSearchMouseClicked
        // TODO add your handling code here:
        File file =new File("lists.txt");
        
        try{
            BufferedReader reader = new BufferedReader(new FileReader("lists.txt"));
            String firstLine = reader.readLine().trim();
            String [] columnName = firstLine.split("\n");
            DefaultTableModel model = (DefaultTableModel)table.getModel();
            
            Object[] tableLines = reader.lines().toArray();
            for(int i = 0;i < tableLines.length;i++)
            {
                String line = tableLines[i].toString().trim();
                String[] dataRow = line.split("\t");
                model.addRow(dataRow);
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_labelSearchMouseClicked

